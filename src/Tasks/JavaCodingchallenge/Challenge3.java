package Tasks.JavaCodingchallenge;

public class Challenge3 {
    public static void main(String[] args) {
        int a=10;
        int b= 5;
//- Use comparison operators (==, !=, <, >, <=, >=)
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a<b);
        System.out.println(a>b);
        System.out.println(a<=b);
        System.out.println(a>=b);
      //  - Use logical operators (&&, ||, !)
        System.out.println((a > b) && (a > 0));
        System.out.println((a < b) || (a > 0));
        System.out.println(!(a > b));

    }
}
